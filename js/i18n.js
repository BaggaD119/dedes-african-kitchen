window.currentLang = function(){
  try { return localStorage.getItem('dak-lang') || 'en'; } catch(e){ return 'en'; }
};

window.applyLanguage = function(lang){
  if(!window.I18N[lang]) lang = 'en';
  try { localStorage.setItem('dak-lang', lang); } catch(e){}
  document.documentElement.setAttribute('lang', lang);
  const dict = window.I18N[lang];
  const base = window.I18N.en;

  document.querySelectorAll('[data-i18n]').forEach(el=>{
    const key = el.getAttribute('data-i18n');
    const val = dict[key] ?? base[key];
    if(val !== undefined) el.textContent = val;
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el=>{
    const key = el.getAttribute('data-i18n-placeholder');
    const val = dict[key] ?? base[key];
    if(val !== undefined) el.setAttribute('placeholder', val);
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el=>{
    const key = el.getAttribute('data-i18n-title');
    const val = dict[key] ?? base[key];
    if(val !== undefined) el.setAttribute('title', val);
  });
  document.querySelectorAll('[data-i18n-aria]').forEach(el=>{
    const key = el.getAttribute('data-i18n-aria');
    const val = dict[key] ?? base[key];
    if(val !== undefined) el.setAttribute('aria-label', val);
  });

  const pill = document.getElementById('langPillVal');
  if(pill) pill.textContent = window.I18N_LABELS[lang] || 'English';
};

window.t = function(key){
  const lang = window.currentLang();
  const dict = window.I18N[lang] || window.I18N.en;
  return dict[key] ?? window.I18N.en[key] ?? key;
};

document.addEventListener('DOMContentLoaded', function(){
  window.applyLanguage(window.currentLang());
});
